import java.util.Arrays;

public class Example {

    public int plus(int a, int b) {
        return a + b;
    }

    public int minFunction(int n1, int n2) {
        int min;
        if (n1 > n2)
            min = n2;
        else
            min = n1;

        return min;
    }

    public int division(int a, int b) {
        return a / b;
    }
}


